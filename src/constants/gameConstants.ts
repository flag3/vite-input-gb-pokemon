// 特殊文字の判定
export const isControlChar = (char: string): boolean => {
  return char === "ED" || char === "カナ" || char === "かな" || char === "ていせい" || char === "けってい";
};

export const isDiacriticalMark = (char: string): boolean => {
  return char === "゛" || char === "゜" || char === "ー" || char === "リ" || char === "へ";
};

